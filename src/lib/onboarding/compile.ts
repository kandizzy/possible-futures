import type { IntakeAnswers, IntakeRole, IntakeProject, IntakeVersion } from '../types';

/**
 * Compile intake answers into the three markdown documents the app expects.
 * The headers must match what src/lib/parsers/compass.ts and src/lib/ai/materials.ts
 * (extractSection, extractRules) look for — changing them will break scoring and
 * materials generation silently.
 */
export function compileIntake(answers: IntakeAnswers): {
  book: string;
  compass: string;
  playbook: string;
} {
  return {
    book: compileBook(answers),
    compass: compileCompass(answers),
    playbook: compilePlaybook(answers),
  };
}

// ----------------------------------------------------------------------------
// Book
// ----------------------------------------------------------------------------

function compileBook(a: IntakeAnswers): string {
  const out: string[] = [];

  out.push(`# ${a.name || 'Your Name'}: Possible Futures`);
  out.push('');
  out.push('_Generated by Possible Futures, First Printing._');
  out.push('');

  // Contact
  out.push('## Contact');
  out.push('');
  if (a.name) out.push(`- **Name:** ${a.name}`);
  if (a.email) out.push(`- **Email:** ${a.email}`);
  if (a.location) out.push(`- **Location:** ${a.location}`);
  if (a.website) out.push(`- **Website:** ${a.website}`);
  if (a.education && a.education.length > 0) {
    out.push('- **Education:**');
    for (const ed of a.education) {
      const year = ed.year ? `, ${ed.year}` : '';
      out.push(`  - ${ed.degree}, ${ed.school}${year}`);
    }
  }
  out.push('');

  // Positioning
  out.push('## Positioning');
  out.push('');
  out.push(a.through_line || '_(not yet written)_');
  out.push('');

  // AI Scoring Context — used by the prompt builder
  out.push('## AI Scoring Context');
  out.push('');
  out.push(
    'The following shapes how this candidate evaluates opportunities. Consider these bullets when scoring job postings.',
  );
  out.push('');
  if (a.current_situation) {
    out.push(`- **Current situation:** ${a.current_situation}`);
  }
  if (a.through_line) {
    out.push(`- **Through-line:** ${a.through_line}`);
  }
  if (a.dream_role) {
    out.push(`- **Dream role:** ${a.dream_role}`);
  }
  if (a.compensation_floor) {
    out.push(`- **Compensation floor:** $${formatMoney(a.compensation_floor)}`);
  }
  if (a.location_constraint) {
    const remote = a.remote_ok === false ? ' (on-site preferred)' : '';
    out.push(`- **Location:** ${a.location_constraint}${remote}`);
  }
  out.push('');

  // Career Timeline
  out.push('## Career Timeline');
  out.push('');
  if (!a.roles || a.roles.length === 0) {
    out.push('_(no roles entered yet)_');
  } else {
    for (const role of a.roles) {
      out.push(...compileRole(role));
    }
  }
  out.push('');

  // Independent Projects
  out.push('## Independent Projects');
  out.push('');
  out.push('_Personal, freelance, or open source work outside of primary employment._');
  out.push('');
  if (!a.projects || a.projects.length === 0) {
    out.push('_(no projects entered yet)_');
  } else {
    for (const p of a.projects) {
      out.push(...compileProject(p));
    }
  }
  out.push('');

  // Recognition
  out.push('## Recognition');
  out.push('');
  if (!a.recognition || a.recognition.length === 0) {
    out.push('_(none listed)_');
  } else {
    for (const item of a.recognition) {
      out.push(`- ${item}`);
    }
  }
  out.push('');

  // Writing Rules — extractRules looks for this header
  out.push('## Writing Rules');
  out.push('');
  out.push('Mandatory voice and style rules for all generated resume and cover letter content.');
  out.push('');
  for (const rule of DEFAULT_WRITING_RULES) {
    out.push(`- ${rule}`);
  }
  if (a.voice_samples && a.voice_samples.length > 0) {
    out.push('');
    out.push('**Phrases that sound like me (for voice calibration):**');
    for (const phrase of a.voice_samples) {
      out.push(`- "${phrase}"`);
    }
  }
  out.push('');

  // Things to Never Say — extractRules looks for this header
  out.push('## Things to Never Say');
  out.push('');
  out.push('Words and phrases that must never appear in generated content, regardless of context.');
  out.push('');
  const bannedWords = (a.banned_words && a.banned_words.length > 0)
    ? a.banned_words
    : DEFAULT_BANNED_WORDS;
  for (const word of bannedWords) {
    out.push(`- "${word}"`);
  }
  out.push('');

  // Sensitive Context — private framing notes
  out.push('## Sensitive Context');
  out.push('');
  out.push('_Private notes on how to frame sensitive parts of this candidate\'s story. Never copy these verbatim into output — use them as guidance only._');
  out.push('');
  if (a.sensitive_context) {
    out.push(a.sensitive_context);
  } else {
    out.push('_(none)_');
  }
  out.push('');

  // Version Strategy — references what's in the Playbook
  out.push('## Version Strategy');
  out.push('');
  out.push('Resume versions and how to choose between them. See APPLICATION_PLAYBOOK.md for the full Role-Type Emphasis Guide.');
  out.push('');
  if (a.versions && a.versions.length > 0) {
    for (const v of a.versions) {
      out.push(`- **Version ${v.letter} — ${v.label}:** ${v.emphasis}`);
    }
  } else {
    for (const v of DEFAULT_VERSIONS) {
      out.push(`- **Version ${v.letter} — ${v.label}:** ${v.emphasis}`);
    }
  }
  out.push('');

  return out.join('\n');
}

function compileRole(role: IntakeRole): string[] {
  const out: string[] = [];
  const end = role.end && role.end.trim() ? role.end : 'Present';
  out.push(`### ${role.title} · ${role.company}`);
  out.push(`_${role.start} – ${end}_`);
  out.push('');
  if (role.summary) {
    out.push(role.summary);
    out.push('');
  }
  if (role.proudest) {
    out.push(`**Proudest of:** ${role.proudest}`);
    out.push('');
  }
  if (role.stack) {
    out.push(`**Stack:** ${role.stack}`);
    out.push('');
  }
  if (role.raw_notes && role.raw_notes.trim()) {
    out.push(role.raw_notes.trim());
    out.push('');
  }
  return out;
}

function compileProject(p: IntakeProject): string[] {
  const out: string[] = [];
  out.push(`### ${p.title}`);
  out.push('');
  if (p.description) {
    out.push(p.description);
    out.push('');
  }
  if (p.stack) {
    out.push(`**Tech:** ${p.stack}`);
    out.push('');
  }
  if (p.serves_versions && p.serves_versions.length > 0) {
    out.push(`**Serves version(s):** ${p.serves_versions.join(', ')}`);
    out.push('');
  }
  return out;
}

