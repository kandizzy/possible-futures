// ----------------------------------------------------------------------------

function compileCompass(a: IntakeAnswers): string {
  const out: string[] = [];

  out.push('# Job Search Compass');
  out.push('');
  out.push('_Generated by Possible Futures, First Printing. The rules below shape every job evaluation._');
  out.push('');

  // The question
  out.push('## What Am I Looking For?');
  out.push('');
  out.push(a.dream_role || '_(not yet written)_');
  out.push('');

  // Role tiers
  if (a.role_tiers) {
    out.push('## Role Tiers');
    out.push('');
    out.push('### Tier 1 — Dream');
    for (const r of a.role_tiers.dream || []) out.push(`- ${r}`);
    if ((a.role_tiers.dream || []).length === 0) out.push('_(none)_');
    out.push('');
    out.push('### Tier 2 — Strong Fit');
    for (const r of a.role_tiers.strong || []) out.push(`- ${r}`);
    if ((a.role_tiers.strong || []).length === 0) out.push('_(none)_');
    out.push('');
    out.push('### Tier 3 — Acceptable');
    for (const r of a.role_tiers.acceptable || []) out.push(`- ${r}`);
    if ((a.role_tiers.acceptable || []).length === 0) out.push('_(none)_');
    out.push('');
  }

  // Constraints — must contain "**Compensation floor:**" pattern for parser
  out.push('## Constraints');
  out.push('');
  if (a.compensation_floor !== undefined && a.compensation_floor !== null) {
    out.push(`- **Compensation floor:** $${formatMoney(a.compensation_floor)}`);
  } else {
    out.push('- **Compensation floor:** $0');
  }
  if (a.location_constraint) {
    out.push(`- **Location:** ${a.location_constraint}`);
  }
  if (a.remote_ok !== undefined) {
    out.push(`- **Remote:** ${a.remote_ok ? 'open' : 'on-site only'}`);
  }
  out.push('');

  // Signal words — parser looks for lines starting with `-` and quoted phrases
  out.push('## Signal Words in Job Postings');
  out.push('');
  out.push('Green flags. Words that mean "pay attention" when they show up in a posting.');
  out.push('');
  if (a.signal_words && a.signal_words.length > 0) {
    for (const word of a.signal_words) {
      out.push(`- "${word}"`);
    }
  } else {
    out.push('- _(none yet)_');
  }
  out.push('');

  // Red flag words — parser looks for the same pattern
  out.push('## Red Flag Words in Job Postings');
  out.push('');
  out.push('Words that mean "reconsider" when they show up in a posting.');
  out.push('');
  if (a.red_flag_words && a.red_flag_words.length > 0) {
    for (const word of a.red_flag_words) {
      out.push(`- "${word}"`);
    }
  } else {
    out.push('- _(none yet)_');
  }
  out.push('');

  out.push('---');
  out.push('');

  // Companies
  out.push('## Companies and Teams to Watch');
  out.push('');
  if (a.target_companies && a.target_companies.length > 0) {
    out.push('### Targets');
    for (const c of a.target_companies) {
      out.push(`- **${c}** — from intake`);
    }
  } else {
    out.push('_(none yet)_');
  }
  out.push('');

  out.push('---');
  out.push('');

  // People — placeholder section (intake doesn't collect people yet)
  out.push('## People Whose Orbits I Want to Be In');
  out.push('');
  out.push('_(none listed — add them from the People page after onboarding)_');
  out.push('');

  out.push('---');
  out.push('');

  // How to score — references the scoring dimensions the app uses
  out.push('## How to Score a Job Posting');
  out.push('');
  out.push('Six dimensions, each scored 0–3:');
  out.push('');
  out.push('| Dimension | Question |');
  out.push('|-----------|----------|');
  out.push('| **Want** | Does the work actually excite me? |');
  out.push('| **Can** | Can I do this well with what I know today? |');
  out.push('| **Grow** | Will I learn something meaningful here? |');
  out.push('| **Pay** | Does compensation meet my floor? |');
  out.push('| **Team** | Will I work with people I respect and learn from? |');
  out.push('| **Impact** | Will what I build actually matter? |');
  out.push('');
  out.push('Max score: 18. Strong fit: 15+. Acceptable: 12–14. Weak: below 12.');
  out.push('');

  return out.join('\n');
}

