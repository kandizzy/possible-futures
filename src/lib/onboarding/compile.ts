// Playbook
// ----------------------------------------------------------------------------

function compilePlaybook(a: IntakeAnswers): string {
  const out: string[] = [];

  out.push('# Application Playbook');
  out.push('');
  out.push('_Generated by Possible Futures, First Printing._');
  out.push('');

  // Resume Rules — extractRules looks for this header
  out.push('## Resume Rules');
  out.push('');
  for (const rule of DEFAULT_RESUME_RULES) {
    out.push(`- ${rule}`);
  }
  out.push('');

  // Cover Letter Rules — extractRules looks for this header
  out.push('## Cover Letter Rules');
  out.push('');
  for (const rule of DEFAULT_COVER_LETTER_RULES) {
    out.push(`- ${rule}`);
  }
  out.push('');

  // Things to Never Do — extractRules looks for this header
  out.push('## Things to Never Do');
  out.push('');
  for (const rule of DEFAULT_NEVER_DO) {
    out.push(`- ${rule}`);
  }
  if (a.banned_words && a.banned_words.length > 0) {
    out.push('- Never use any of these words in generated content:');
    for (const w of a.banned_words) {
      out.push(`  - "${w}"`);
    }
  }
  out.push('');

  // Role-Type Emphasis Guide — getResumeVersionLabels looks for `### Version A: Label`
  out.push('## Role-Type Emphasis Guide');
  out.push('');
  out.push('Which resume version to reach for depending on the role, and what to emphasize.');
  out.push('');
  const versions = (a.versions && a.versions.length > 0) ? a.versions : DEFAULT_VERSIONS;
  for (const v of versions) {
    out.push(`### Version ${v.letter}: ${v.label}`);
    out.push('');
    out.push(v.emphasis || '_(no emphasis notes yet)_');
    if (a.projects) {
      const served = a.projects.filter(
        (p) => p.serves_versions && p.serves_versions.includes(v.letter),
      );
      if (served.length > 0) {
        out.push('');
        out.push('**Emphasize these projects:**');
        for (const p of served) {
          out.push(`- ${p.title}`);
        }
      }
    }
    out.push('');
  }

  return out.join('\n');
}

// ----------------------------------------------------------------------------
// Defaults — generic enough to ship in an open-source release
// ----------------------------------------------------------------------------

function formatMoney(n: number): string {
  return n.toLocaleString('en-US');
}

const DEFAULT_WRITING_RULES = [
  'No em dashes. Use commas or periods.',
  'No "spearheaded," "leveraged," "utilized," "facilitated," "synergy," or "cutting-edge."',
  'No "helped lead." Be specific: either led it or supported it.',
  'Write achievements, not task descriptions.',
  'Put tech stacks in context (e.g., "Built X on Y" not "Proficient in Y").',
  'No inflated or fake metrics. If no number exists, frame the outcome honestly.',
  'Confident but not arrogant. Direct but not robotic.',
  'First person implied (no "I" in bullets), but the voice should feel human.',
];

const DEFAULT_BANNED_WORDS = [
  'spearheaded',
  'leveraged',
  'utilized',
  'facilitated',
  'synergy',
  'cutting-edge',
  'rockstar',
  'ninja',
  'guru',
  'self-taught',
];

const DEFAULT_RESUME_RULES = [
  'Two pages max. No exceptions.',
  'Lead with the clearest title for the role, then a 3-4 sentence summary that mirrors the posting.',
  'Tailor the summary to each role. Never send the same summary twice.',
  'Keep roles in reverse chronological order under Experience.',
  'Use sans-serif fonts (Arial or similar) for ATS compatibility.',
  'No photo, no "References available upon request," no objective statement.',
];

const DEFAULT_COVER_LETTER_RULES = [
  'Three paragraphs max. Long cover letters do not get read.',
  'Role-specific from line one. No career autobiography openers.',
  'Write genuinely different letters for each role.',
  'Lead with insight or specificity, not a narrative of your whole career.',
];

const DEFAULT_NEVER_DO = [
  'Never paste the same cover letter into two applications.',
  'Never promise skills you do not actually have.',
  'Never include a photo, an objective statement, or "References available upon request."',
  'Never send a resume without reading the posting first.',
];

const DEFAULT_VERSIONS: IntakeVersion[] = [
  {
    letter: 'A',
    label: 'Primary',
    emphasis: 'Your default resume. Balanced view of your strongest skills and most relevant experience.',
  },
  {
    letter: 'B',
    label: 'Technical',
    emphasis: 'For roles that emphasize depth. Lead with specific technologies, architectures, and measurable outcomes.',
  },
  {
    letter: 'C',
    label: 'Leadership',
    emphasis: 'For roles where managing people or projects matters. Emphasize team size, cross-functional work, and strategic impact.',
  },
  {
    letter: 'D',
    label: 'Specialist',
    emphasis: 'For roles in a specific niche. Tailor heavily to the domain and cut anything that dilutes the focus.',
  },
];

export const INTAKE_DEFAULTS = {
  writing_rules: DEFAULT_WRITING_RULES,
  banned_words: DEFAULT_BANNED_WORDS,
  resume_rules: DEFAULT_RESUME_RULES,
  cover_letter_rules: DEFAULT_COVER_LETTER_RULES,
  never_do: DEFAULT_NEVER_DO,
  versions: DEFAULT_VERSIONS,
};
